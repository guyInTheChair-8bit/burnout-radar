/**
 * buildActionButtons
 *
 * Returns an Actions block with buttons appropriate to the detected risk profile.
 * Each button maps to an action_id that is handled in action_handlers.ts.
 *
 * Button strategy:
 *  - key_person_dependency  → Review Workload Allocation (primary)
 *  - systemic_crunch_time   → Send Pulse Survey + Draft Comp Time Reminder
 *  - silent_isolation       → Suggest No-Meeting Day (primary)
 *  - none                   → No buttons (empty array)
 */
function buildActionButtons(riskProfile: RiskProfile): Block[] {
  if (riskProfile === "none") {
    return [];
  }

  let elements: Block[] = [];

  switch (riskProfile) {
    case "key_person_dependency":
      elements = [
        {
          type: "button",
          text: { type: "plain_text", text: "Review Workload Allocation", emoji: true },
          style: "primary",
          action_id: "trigger_workload_review",
          value: "workload_review",
        },
      ];
      break;

    case "systemic_crunch_time":
      elements = [
        {
          type: "button",
          text: { type: "plain_text", text: "Send Anonymous Pulse Survey", emoji: true },
          action_id: "trigger_pulse_survey",
          value: "pulse_survey",
        },
        {
          type: "button",
          text: { type: "plain_text", text: "Draft Comp Time Reminder", emoji: true },
          action_id: "draft_comp_time",
          value: "comp_time",
        },
      ];
      break;

    case "silent_isolation":
      elements = [
        {
          type: "button",
          text: { type: "plain_text", text: "Suggest No-Meeting Day", emoji: true },
          style: "primary",
          action_id: "suggest_no_meeting",
          value: "no_meeting_day",
        },
      ];
      break;
  }

  return [
    {
      type: "actions",
      elements,
    },
  ];
}

/**
 * formatDate
 *
 * Converts an ISO-8601 date string (YYYY-MM-DD) to a friendlier display
 * format (e.g. "Monday, July 7, 2025"). Falls back to the raw string if
 * parsing fails.
 */
function formatDate(isoDate: string): string {
  try {
    return new Date(isoDate).toLocaleDateString("en-US", {
      weekday: "long",
      year: "numeric",
      month: "long",
      day: "numeric",
    });
  } catch {
    return isoDate;
  }
}
